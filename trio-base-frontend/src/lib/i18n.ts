export type AppMessages = (typeof messages)[AppLocale];

export function getMessages(locale: AppLocale): AppMessages {
  return messages[locale];
}

export function useI18n() {
  const [hydrated, setHydrated] = useState(false);
  useEffect(() => { setHydrated(true); }, []);

  const rawLocale = usePreferencesStore((state) => state.locale);
  const setLocale = usePreferencesStore((state) => state.setLocale);

  // Before hydration use zh-CN to match SSR output; after hydration,
  // the persisted locale (possibly en-US) can safely run client-only.
  const locale = hydrated ? rawLocale : "zh-CN";

  return {
    locale,
    setLocale,
    messages: getMessages(locale),
  };
}

export function resolveMessage(messages: AppMessages, path: string): string {
  return path.split(".").reduce<unknown>((current, key) => {
    if (current && typeof current === "object" && key in (current as Record<string, unknown>)) {
      return (current as Record<string, unknown>)[key];
    }
    return path;
  }, messages) as string;
}
